<script setup>
import gsap from 'gsap';
import { MotionPathPlugin } from 'gsap/MotionPathPlugin';
import { ScrollTrigger } from 'gsap/ScrollTrigger';
import { onMounted } from 'vue';

gsap.registerPlugin(MotionPathPlugin, ScrollTrigger);

onMounted(() => {
	if (window.innerWidth > 768) {
		const path = document.getElementById('path');
		const pathLength = path.getTotalLength();

		path.style.strokeDasharray = pathLength;
		path.style.strokeDashoffset = pathLength;

		gsap.fromTo(
			'#path',
			{
				strokeDashoffset: pathLength
			},
			{
				strokeDashoffset: 0,
				scrollTrigger: {
					trigger: '#destination',
					start: 'top top',
					end: () => `+=${document.querySelector('#container').scrollWidth}`,
					scrub: 1
				}
			}
		);
		gsap.to('#title', {
			opacity: 0,
			scrollTrigger: {
				trigger: '#destination',
				start: 'top top'
			}
		});
		gsap.set('#plane', { xPercent: -50, yPercent: -50, transformOrigin: '50% 50%' });
		gsap.to('#plane', {
			motionPath: {
				path: '#path',
				align: '#path',
				autoRotate: true
			},
			scrollTrigger: {
				trigger: '#destination',
				start: 'top top',
				end: () => `+=${document.querySelector('#container').scrollWidth}`,
				pin: true,
				scrub: 1
			}
		});
		const places = gsap.utils.toArray('.place');
		places.forEach(place => {
			gsap.to(place, {
				xPercent: -100 * (places.length - 1),
				scrollTrigger: {
					trigger: '#destination',
					start: 'top top',
					end: () => `+=${document.querySelector('#container').scrollWidth}`,
					scrub: 1
				}
			});
		});

		let proxy = { scaleX: 1 },
			scaleXSetter = gsap.quickSetter('.img', 'scaleX'), // fast
			clamp = gsap.utils.clamp(0.7, 1.3); // limit scaleX between 0.8 and 1.2 for a realistic effect.

		ScrollTrigger.create({
			onUpdate: self => {
				let scaleX = clamp(1 + self.getVelocity() / 1000); // adjust the divisor to control the scaleX effect
				// only do something if the scaleX is more severe. Remember, we're always tweening back to 1, so if the user slows their scrolling quickly, it's more natural to just let the tween handle that smoothly rather than jumping to the smaller scaleX.
				if (Math.abs(scaleX - 1) > Math.abs(proxy.scaleX - 1)) {
					proxy.scaleX = scaleX;
					gsap.to(proxy, {
						scaleX: 1,
						duration: 0.8,
						ease: 'power3',
						overwrite: true,
						onUpdate: () => scaleXSetter(proxy.scaleX)
					});
				}
			}
		});

		// make the right edge "stick" to the scroll bar. force3D: true improves performance
		gsap.set('.img', { transformOrigin: 'right center', force3D: true });
	} else {
		gsap.to('#small-plane', {
			scrollTrigger: {
				trigger: '.small__wrapper',
				start: '-=80 top',
				end: 'bottom-=100 top',
				scrub: 1,
				pin: true
			}
		});
		gsap.utils.toArray('.small__box').forEach(box => {
			gsap.from(box, {
				scrollTrigger: {
					trigger: box,
					start: 'top center-=200',
					toggleClass: 'active'
				}
			});
		});
	}
});
</script>

